<p>BonnieCMS e' un CMS minimalista adatto per siti di piccole e medie dimensioni.<br>
Non usa alcun tipo di database tradizionale, i dati vengono memorizzati direttamente su filesystem.</p>
<p>Il fatto che sia minimalista, non vuol dire che lo sia anche nel numero di funzionalita' implementate.<br>
BonnieCMS e' minimalista in quanto la sua architettura e' stata studiata per essere piu' semplice possibile.
</p>
<p>Al momento e' in pieno sviluppo e la versione disponibile si arricchira' di molte nuove funzionalita', quindi
ci vorra' del tempo prima che diventi realmente stabile.</p>
<p>BonnieCMS e' totalmente gratuito, e viene rilasciato sotto licenza BSD.</p>
<h3>Caratteristiche</h3>
<ul>
	<li>creazione immediata di nuove pagine</li>
	<li>sistema di template</li>
	<li>estendibile tramite plugin</li>
	<li>requisiti minimi di installazione (solo PHP > 5.2)</li>
</ul>
